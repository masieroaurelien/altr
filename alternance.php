			<title class="alternanceart">Les 2 contrats d’alternance</title>
			<img src="medias/alternance_contrats.jpg" class="alternanceart"></img>
			<p class="alternanceart">Il existe 2 types de contrats en alternance : le contrat d'apprentissage et le contrat de professionnalisation. Pour savoir si tu es éligibles à l’un, l'autre ou les deux, rapproche-toi de ton école. Cette info te sera demandée par les entreprises avec lesquelles tu seras en contact lors de tes recherches.

			Le contrat d’apprentissage et le contrat de professionnalisation sont conclus en contrat à durée limitée ou dans le cadre d’un contrat à durée indéterminée (CDI).

			Le contrat d'apprentissage

			Âge : Accessible de 16 à 29 ans révolus, à l’exception des apprentis préparant un diplôme ou titre supérieur à celui obtenu, des travailleurs handicapés, des personnes ayant un projet de création ou de reprise d’entreprise et des sportifs de haut niveau.
			Employeurs : Les entreprises relevant du secteur artisanal, commercial, industriel, agricole ainsi que les employeurs du milieu associatif et des professions libérales + Les employeurs du secteur public non industriel et commercial (fonctions publiques d'État, territoriale et hospitalière, ainsi que les établissements publics administratifs)
			Durée : 2 ans en principe, mais dans certains cas dérogatoires, elle peut être inférieure (6 mois) ou supérieure (3 ans). Elle est de 4 ans pour les travailleurs handicapés. La durée de la formation représente au moins 25 % de la durée totale du contrat ou de la période d’apprentissage.
			Rémunération : Variable en fonction de l’âge de l’alternant et progressive à chaque nouvelle année d’exécution du contrat. Le salaire minimum perçu par l’apprenti correspond à un % du SMIC ou du SMC (salaire minimum conventionnel de l’emploi occupé) pour les plus de 21 ans.
			Le contrat de professionnalisation

			Âge : Accessible de 16 à 25 ans révolus, à l’exception des demandeurs d’emploi, des bénéficiaires du revenu de solidarité active (RSA) ou de l’allocation de solidarité spécifique (ASS) ou de l’allocation aux adultes handicapés (AAH) ou de l’allocation de parent isolé (API) dans les DOM et les collectivités de Saint-Barthélemy, Saint-Martin et Saint-Pierre-et-Miquelon, des personnes ayant bénéficié d’un contrat aidé.
			Employeurs : Toute entreprise issue du secteur privé et cotisant auprès des OPCO.
			Durée : De 6 mois à 1 an, voire 3 ans dans certains cas. La durée de la formation représente entre 15 à 25 % de la durée du contrat, sans pouvoir être inférieure à 150 heures, sauf dérogation de branche.
			Rémunération : Variable en fonction de l’âge de l’alternant et du niveau de sa formation initiale.</p>
