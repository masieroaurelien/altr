			<img src="medias/alternance_alternance.jpg" class="alternanceart">
			<p class="alternanceart">L'alternance, c'est une nouvelle vie qui commence!

			Tu seras salarié dès ton premier jour, un salarié en formation certes, mais un salarié à part entière. Désormais,ton temps sera partagé entre ton lieu de travail et ton lieu de formation.

			Entre théorie et pratique, l'alternance permet donc de se former à un métier, à la fois à l'école et au sein d'une entreprise. Véritable tremplin vers l'emploi et l'insertion professionnelle, l'alternance est une expérience valorisée sur un CV.

			Quel que soit le type de contrat choisi, apprentissage ou professionnalisation, l’objectif est l’obtention d’un diplôme et une montée en compétences rapides. La durée de l'alternance est de minimum 6 mois et de maximum 3 ans, hors cas particulier.

			Comme tout contrat de travail, l'alternance donne droit à une rémunération qui peut varier selon ton âge, ton contrat et ton niveau d’études. À ce salaire, peuvent s’ajouter les avantages habituels d’une entreprise comme les tickets restaurants, les primes, un treizième mois, une mutuelle etc. Bien évidemment, tu auras le droit à 25 jours de congés payés annuels comme tes autres collègues.

			L’alternant est un salarié à part entière. À ce titre, les lois, les règlements et la convention collective de la branche professionnelle et celle de l’entreprise lui sont applicables dans les mêmes conditions qu’aux autres salariés. Le temps de travail est identique à celui des autres salariés. L’employeur doit permettre à l’alternant de suivre les cours théoriques professionnels. Ce temps de formation en CFA est compris dans le temps de travail effectif.

			Certaines régions peuvent aussi proposer des aides à la mobilité pour financer une partie de tes frais de transport, voire les frais liés à un déménagement pour une alternance.</p>
