			<img src="medias/alternance_alternance.jpg" class="alternanceart">
			<p class="alternanceart">
				L'alternance, c'est une nouvelle vie qui commence!
			</p>
			<p class="alternanceart"> 
				<strong>Tu seras salarié dès ton premier jour</strong>, un salarié en formation certes, mais un salarié à part entière. Désormais,ton temps sera partagé entre ton lieu de 
				travail et ton lieu de formation. 
			</p>
			<p class="alternanceart">
				Entre théorie et pratique, l'alternance permet donc de <strong>se former à un métier</strong>, à la fois à l'école et au sein d'une entreprise. Véritable tremplin vers l'emploi 
				et l'insertion professionnelle, l'alternance est <strong>une expérience valorisée sur un CV</strong>. 
			</p>
			<p class="alternanceart"> 
    			Quel que soit le type de contrat choisi, apprentissage ou professionnalisation, <strong>l’objectif est l’obtention d’un diplôme et une montée en compétences rapides</strong>. 
				La durée de l'alternance est de minimum 6 mois et de maximum 3 ans, hors cas particulier. 
			</p>
			<p class="alternanceart">
				Comme tout contrat de travail, <strong>l'alternance donne droit à une rémunération</strong> qui peut varier selon ton âge, ton contrat et ton niveau d’études. À ce salaire, 
				peuvent s’ajouter les avantages habituels d’une entreprise comme les tickets restaurants, les primes, un treizième mois, une mutuelle etc. Bien évidemment, tu auras le droit à 
				25 jours de congés payés annuels comme tes autres collègues. 
			</p>
			<p class="alternanceart">
				L’alternant est un salarié à part entière. À ce titre, les lois, les règlements et la convention collective de la branche professionnelle et celle de l’entreprise lui sont 
				applicables dans les mêmes conditions qu’aux autres salariés. Le temps de travail est identique à celui des autres salariés. L’employeur doit permettre à l’alternant de suivre 
				les cours théoriques professionnels. Ce temps de formation en CFA est compris dans le temps de travail effectif. 
			</p>
			<p class="alternanceart">
				Certaines régions peuvent aussi proposer <strong>des aides à la mobilité</strong> pour financer une partie de tes frais de transport, voire les frais liés à un déménagement 
				pour une alternance.
			</p> 
		</div>

		<div class="container" id="contrats">
			<title class="alternanceart">Les 2 contrats d’alternance</title>
			<img src="medias/alternance_contrats.jpg" class="alternanceart"></img>
			<p class="alternanceart">
				Il existe 2 types de contrats en alternance : <strong>le contrat d'apprentissage et le contrat de professionnalisation</strong>. Pour savoir si tu es éligibles à l’un, l'autre 
				ou les deux, rapproche-toi de ton école. Cette info te sera demandée par les entreprises avec lesquelles tu seras en contact lors de tes recherches. 
			</p>
			<p class="alternanceart">
				Le contrat d’apprentissage et le contrat de professionnalisation sont conclus en contrat à durée limitée ou dans le cadre d’un contrat à durée indéterminée (CDI).
			</p>
			<p class="alternanceart">Le contrat d'apprentissage</p>
    			<ul class="alternanceart">	
					<li class="alternanceart"><strong>Âge : Accessible de 16 à 29 ans révolus</strong>, à l’exception des apprentis préparant un diplôme ou titre supérieur à celui obtenu, des 
						travailleurs handicapés, des personnes ayant un projet de création ou de reprise d’entreprise et des sportifs de haut niveau.</li>
					<li class="alternanceart"><strong>Employeurs :</strong> Les entreprises relevant du secteur artisanal, commercial, industriel, agricole ainsi que les employeurs du milieu 
						associatif et des professions libérales + Les employeurs du secteur public non industriel et commercial (fonctions publiques d'État, territoriale et hospitalière, ainsi 
						que les établissements publics administratifs).</li>
					<li class="alternanceart"><strong>Durée : 2 ans en principe</strong>, mais dans certains cas dérogatoires, elle peut être inférieure (6 mois) ou supérieure (3 ans). Elle est 
						de 4 ans pour les travailleurs handicapés. La durée de la formation représente au moins 25 % de la durée totale du contrat ou de la période d’apprentissage.</li>
					<li class="alternanceart"><strong>Rémunération :</strong> Variable en fonction de l’âge de l’alternant et progressive à chaque nouvelle année d’exécution du contrat. Le 
						salaire minimum perçu par l’apprenti correspond à un % du SMIC ou du SMC (salaire minimum conventionnel de l’emploi occupé) pour les plus de 21 ans.</li>
    			</ul>
    		<p class="alternanceart">Le contrat de professionnalisation</p>
    			<ul class="alternanceart">
					<li class="alternanceart"><strong>Âge : Accessible de 16 à 25 ans révolus</strong>, à l’exception des demandeurs d’emploi, des bénéficiaires du revenu de solidarité active 
						(RSA) ou de l’allocation de solidarité spécifique (ASS) ou de l’allocation aux adultes handicapés (AAH) ou de l’allocation de parent isolé (API) dans les DOM et les 
						collectivités de Saint-Barthélemy, Saint-Martin et Saint-Pierre-et-Miquelon, des personnes ayant bénéficié d’un contrat aidé. </li>
    				<li class="alternanceart"><strong>Employeurs :</strong> Toute entreprise issue du secteur privé et cotisant auprès des OPCO.</li>
					<li class="alternanceart"><strong>Durée :</strong> De 6 mois à 1 an, voire 3 ans dans certains cas. La durée de la formation représente entre 15 à 25 % de la durée du contrat, 
						sans pouvoir être inférieure à 150 heures, sauf dérogation de branche.</li>
    				<li class="alternanceart"><strong>Rémunération :</strong> Variable en fonction de l’âge de l’alternant et du niveau de sa formation initiale.</li>
    			</ul>
