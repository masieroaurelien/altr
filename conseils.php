			<title class="conseilsart">Réussir son entretien d’embauche pour une alternance</title>
			<img src="medias/article3.jpg" class="conseilsart">
			<p class="conseilsart">
				Après avoir travaillé ton réseau, toqué aux portes, envoyé ton CV, relancé… <strong>Tu as finalement reçu l’appel que tu attendais tant !</strong> Une entreprise est 
				intéressée par ton profil et souhaite discuter de ta candidature. La question que tu te poses maintenant, c’est <strong>comment cartonner et montrer que tu es la personne 
				qu’il leur faut ?</strong> 
			</p>
			<p class="conseilsart">
				Si l’intention est louable, la première chose à faire est de garder en tête que réussir un entretien d’embauche, ce n’est pas seulement signer un contrat au bout du 
				chemin. C’est aussi et surtout <strong>vérifier que l’entreprise que tu rejoindras te proposera un cadre de travail satisfaisant et enrichissant pour mener à bien ton 
				alternance.</strong>
			</p>
			<p class="conseilsart">
				Cette perspective va vraiment t’aider à <strong>relativiser</strong> : après tout, les entreprises sont elles aussi des candidates. En effet, si tes interlocuteurs se 
				montrent hautains ou volontairement déstabilisants… Est-ce si grave de rater ton entretien et ne pas travailler avec/pour eux ?
			</p>
			<p class="conseilsart">
				Aussi, on devrait plutôt parler des entretiens d’embauche, plutôt que de l’entretien d’embauche. En effet, il en existe 2 types : celui de <strong>présélection par 
				téléphone</strong> et ceux plus « serious business » <strong>en physique ou en visio</strong> avec différents interlocuteurs (recruteur, manager opérationnel, CEO etc.) 
				bien souvent accompagnés de <strong>tests techniques.</strong>
			</p>
			<p class="conseilsart">
				On fait le point sur ces différentes étapes et comment s’armer pour les appréhender sereinement, en toute confiance ! 
			</p>
			<h2 class="conseilsart"><strong>1. Prépare un pitch en béton pour la présélection par téléphone</strong></h2>
			<p class="conseilsart">
				Génial ! Ton téléphone sonne. Règle n°1 : <strong>ne décroche pas</strong>. Ce n’est même pas une blague. En général, il vaut mieux que le recruteur laisse un message 
				sur ton répondeur. Cela te permettra de filer sur l’annonce, l’étudier sous toutes ses coutures et <strong>structurer au calme et à tête reposée ta réponse à la 
				sempiternelle question « Présentez-vous »</strong> qui va t’être posée dans 99,99% des cas. 
			</p>
			<p class="conseilsart">
				90% des candidats se lancent alors dans <strong>une très ennuyeuse redite de leur CV</strong> : « j’ai eu tel bac en telle année, j’ai fait telle école pour tel diplôme, 
				j’ai eu telle 1ère expérience chez telle entreprise etc. » Breaking news Le recruteur a déjà lu ta candidature, il sait déjà tout ça. 
			</p>
			<p class="conseilsart">
				En effet, quand il te téléphone, il cherche à <strong>valider la pertinence de ton profil</strong> par rapport à l’offre, à <strong>s’assurer que tu correspondes bien 
				aux attentes générales et à cerner ta personnalité</strong>. Pour cela, avant de le rappeler, tu vas préparer une réponse punchy et réfléchie qui sort de l’ordinaire.
			</p>
			<p class="conseilsart">
				Cette réponse, c’est ce que l’on appelle <strong>le pitch</strong>. C’est un complément plus humain et plus chaleureux à ta candidature.
			</p>
			<p class="conseilsart">
				En 5 minutes, tu vas te décrire et montrer que tu as bien compris le poste en abordant ces 4 points : 
			</p>
			<ul class="conseilsart">
				<li class="conseilsart"><strong>Tes objectifs</strong>. En quelques phrases, explique clairement pourquoi tu as choisi ce métier, pourquoi tu veux faire une alternance 
					et le cas échéant, pourquoi une reconversion professionnelle ?</li>
				<li class="conseilsart"><strong>Tes compétences</strong>. Choisis 2 ou 3 compétences mentionnées dans l’annonce et explique en quoi tu les possèdes et pourquoi tu seras 
					capable de les mobiliser rapidement ; ou alors pourquoi ça t’intéresse de les développer. </li>
				<li class="conseilsart"><strong>Tes forces</strong>. Parle de tes qualités de façon plus personnelles, de ces petits trucs en plus qui peuvent te démarquer de tes 
					concurrents.</li>
				<li class="conseilsart"><strong>Ta motivation</strong>. Termine par une petite formule clé qui résume ton enthousiasme à l’idée de rejoindre cette entreprise !</li>
			</ul>
			<p class="conseilsart">
				Voilà pourquoi il est important de prendre le temps de <strong>répéter et d’adapter ton pitch à chaque entreprise</strong>, avant même de l’avoir au bout du fil. 
			</p>
			<p class="conseilsart">
				La suite de l’appel se portera sur <strong>des questions très généralistes</strong>, comme ta date de disponibilité pour le démarrage, ta mobilité géographique ou encore 
				les modalités de ton alternance. Sois sûr(e) de connaître toutes ces informations (type de contrat, rythme de l’alternance etc.) ou de savoir comment les obtenir. 
			</p>
			<h2 class="conseilsart"><strong>2. Reste toi-même tout au cours du processus</strong></h2>
			<p class="conseilsart">
				C’est un conseil tout bête mais que beaucoup oublient : reste toi-même. Un conseil tout bête, mais pourtant le meilleur qu’on ait en stock. En jouant au candidat idéal, 
				ça finira par te porter préjudice à un moment ou à un autre. On a tous en tête ce running gag de la personne qui s’invente une maîtrise bilingue de l’anglais sur son CV 
				et qui se met à balbutier quand le recruteur lance : « Let’s switch in English! » 
			</p>
			<p class="conseilsart">
				<strong>Considère chaque entretien comme un échange honnête et fidèle à la réalité</strong>. Ne sous-estime pas tes qualités et ce que tu sais faire, mais ne t’invente 
				pas non plus des compétences que tu n’as pas.
			</p>
			<p class="conseilsart">
				Tu peux la jouer franco et dire que tu ne sais pas faire telle ou telle chose, mais que tu as l’envie d’apprendre (d’où l’alternance). Profite en aussi pour montrer que 
				tu as déjà un plan d’action pour progresser sur tes lacunes. Cela ne fera que <strong>renforcer ta motivation, ta débrouillardise et ton volontarisme</strong>. En 
				embauchant un alternant, ton manager est bien conscient que tu seras là pour apprendre, sinon il se tournerait vers des profils seniors. 
			</p>
			<p class="conseilsart">
				Bon par contre comme toujours, il y a des exceptions : si tu es de nature « toujours à la bourre », le jour de l’entretien, fais un effort et arrive quelques minutes en avance. 
			</p>
			<h2 class="conseilsart"><strong>3. Pose toujours des questions en entretien</strong></h2>
			<p class="conseilsart">
				Qu’il soit physique ou en visio, un entretien se déroule souvent de cette façon : 
			</p>
			<ul class="conseilsart">
				<li class="conseilsart">La personne qui conduit l’entretien te <strong>présente l’entreprise</strong> et son rôle au sein de celle-ci.</li>
				<li class="conseilsart">Tu <strong>te présentes</strong> à ton tour (à l’aide du pitch que tu peux étoffer !).</li>
				<li class="conseilsart">Vous entrez tous les deux <strong>en détails dans le vif du sujet</strong> : le poste, les missions et les compétences  techniques attendues.</li>
				<li class="conseilsart">Tu as l’occasion de <strong>poser des questions</strong> en fin d’entretien.</li>
				<li class="conseilsart">L’entretien peut également se poursuivre par <strong>un test de compétences</strong>, une étude de cas ou autre exercice visant à vérifier que tes 
					compétences soit bien réelles (d’où l’importance de rester soi-même).</li>
			</ul>
			<p class="conseilsart">
				Ce qui nous intéresse ici, c’est la partie <strong>questions/réponses</strong>. Car tout comme le fameux « Présentez-vous », tu peux tabler sur 99,99% de chances que l’on 
				te demande si tu en as. Et si tu réponds « non », tu vas laisser une très mauvaise impression à ton ou tes interlocuteur(s). 
			</p>
