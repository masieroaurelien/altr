			<title class="conseilsart">Les meilleures startégies pour décrocher une alternance</title>
			<img src="medias/article1.jpg" class="conseilsart">
			<p class="conseilsart">
				Félicitations ! Une école vient de valider ton inscription pour <strong>commencer LE parcours diplômant qui te formera au futur métier de tes rêves</strong>. Garde cette joie et 
				cette belle énergie qui en découle pour commencer <strong>ta recherche d’alternance dans un bon état d’esprit</strong>. C’est le fuel dont tu auras besoin pour affronter ce qui 
				t’attend…
    		</p>
    		<p class="conseilsart">
				Car on ne va pas se mentir, <strong>trouver une école pour une alternance, ce n’est pas bien compliqué</strong>. Ça se corse après, pour trouver l’entreprise qui t'accueillera 
				durant le volet pratique de ta formation. 
    		</p>
    		<p class="conseilsart">
				Une recherche d’alternance, au même titre que toute recherche d’emploi (pour un job qui te fait vibrer, on ne parle pas ici des petits boulots étudiants), c’est une grosse partie 
				d’organisation, de remise en question, d’espoirs et de doutes. 
			</p>
			
			<h2 class="conseilsart"><strong>1. Adopte le bon état d’esprit</strong></h2>
			<p class="conseilsart">
				C’est un fait, il y a <strong>beaucoup de concurrence sur les postes en alternance</strong>. En 2019, on comptait 491 000 apprentis en France. C’est <strong>18% des étudiants du 
				supérieur</strong> et depuis ces dernières années, ces chiffres sont <strong>en constante augmentation</strong> grâce à un gros travail de démocratisation de l’alternance.
			</p>
			<p class="conseilsart">
				Cependant, selon une enquête Studyrama, <strong>84% des étudiants sondés considèrent que cette recherche est difficile</strong> et même <strong>stressante pour 62% d’entre eux</strong>. 
				C’est normal, un projet d’alternance est un projet d’avenir que l’on veut réussir : <em class="conseilsart">enjeu important pour le futur = investissement émotionnel fort = 
				beaucoup de pression</em>.
			</p>
			<p class="conseilsart">
				Aussi et parce qu’on est humain tout simplement, <strong>on ADORE se conforter dans les fausses excuses et les illusions</strong> qui nous arrangent bien :
			</p>
			<ul class="conseilsart">
				<li class="conseilsart">La conjecture actuelle est difficile (COVID-19 c’est de toi qu’on parle).</li>
				<li class="conseilsart">Je n’habite pas en IDF alors que tout se passe à Paris.</li>
				<li class="conseilsart">Les entreprises ne recrutent pas en alternance dans mon domaine.</li>
				<li class="conseilsart">Les recruteurs veulent tous des juniors avec 35 ans d’expérience.</li>
				<li class="conseilsart">Etc., etc., etc.</li>
			</ul>
			<p class="conseilsart">
				<strong>Les raisons de ne pas trouver d’alternance sont multiples</strong> (ou de croire que l’on ne peut pas y arriver). Même si certains de tes ressentis peuvent se confirmer 
				sur le terrain, il faut garder en tête que <strong>LA solution pour trouver une alternance, il n’y en a qu’une : TOI & TES ACTIONS</strong>.
			</p>
			<p class="conseilsart">
				Alors respire un bon coup, donne-toi du courage et une bonne dose de confiance. Ce seront tes meilleurs potes pour garder la pêche au cours de <strong>cette aventure endiablée de 
				la recherche d’alternance</strong>. 
			</p>

			<h2 class="conseilsart"><strong>2. Décris ton alternance idéale de façon ultra détaillée</strong></h2>
			<p class="conseilsart">
				Quand on cherche un job, il y a ce qu’on idéalise, ce que l’on veut vraiment et ce qui existe dans la vraie vie réelle. Pour commencer par trinquer avec un verre à moitié plein, 
				prends le temps d’écrire noir sur blanc la liste la plus exhaustive possible des différentes composantes de ton alternance idéale, en laissant librement parler tes envies les 
				plus folles.
			</p>
			<p class="conseilsart">
				Voici quelques points à explorer :
			</p>
			<p class="conseilsart">
				<strong>Le nom du poste convoité et de toutes ses variantes et déclinaisons</strong>. Tu peux même inclure certains métiers satellites au tien. Même s’ils sont un peu différents 
				de ce que tu recherches, certaines annonces peuvent se tromper dans l’intitulé, mais décrire des missions en lien avec ta formation. Ces confusions ne sont pas volontaires, mais 
				il faut les anticiper pour ne pas passer à côté d’un job qui pourrait quand même te correspondre. Ex : le community manager peut aussi être appelé social media manager, content 
				manager, chargé des réseaux sociaux, chargé de communication digitale etc.
			</p>
			<p class="conseilsart">
				<strong>Les compétences que tu as déjà et que tu pourras mobiliser dès ta prise de poste</strong>. Même si tu crois que tu n’as pas encore d’expérience, il y a forcément des 
				savoir-faire et des savoir-être que tu as acquis lors de projets passés (dans ta vie pro, projets scolaires, initiatives perso, bénévolat, etc.)
			</p>
			<p class="conseilsart">
				<strong>Les compétences que tu voudras développer au fil des mois</strong>, en fonction de ce que tu apprendras au cours de ta formation et de l’évolution de tes missions.
			</p>
			<p class="conseilsart">
				<strong>Les secteurs dans lesquels tu as envie de bosser et ceux que tu veux à tout prix éviter</strong>. Bien souvent, c’est en lien avec notre personnalité et nos valeurs, ce 
				qu’on aime et déteste dans la vie en général. Attention cependant aux idées reçues, renseigne-toi bien avant d’être trop catégorique.
			</p>
			<p class="conseilsart">
				<strong>La taille de l’entreprise</strong>. CAC40, PME, start ups… Les méthodes de travail sont bien spécifiques dans chaque environnement, ce qui fit avec certains mais pas 
				avec d’autres.
			</p>
			<p class="conseilsart">
				<strong>Le type de management et d’ambiance de travail qui te conviennent</strong>. Pareil que pour le point précédent. Si tu es en reconversion professionnelle, tu as déjà eu 
				plein de boss et de collègues. Essaie de bien réfléchir aux personnalités aux côtés desquelles tu pourras t’épanouir. Ce serait dommage que ton alternance soit morose parce que 
				l’ambiance ne te convient pas. 
			</p>
			<p class="conseilsart">
				<strong>La zone géographique : jusqu’où es-tu prêt à bouger ?</strong> Surtout si tu n’as pas rejoint une école en ligne qui te donne la possibilité de suivre des cours à distance, 
				c’est une contrainte qu’il faut absolument prendre en compte. Le rythme de l’alternance est soutenu, le temps de transport est un facteur qui pourra directement influer sur ta 
				réussite et ton bien-être au quotidien. Sache aussi qu’à la différence d’un stage, l’alternance c’est comme la baguette : une spécialité française. Oublie l’international — à 
				l’exception des boîtes tricolores qui ont des filiales à l’étranger ou des programmes d’échange spécifiques à certaines écoles bien sûr. 
			</p>
			<p class="conseilsart">
				<strong>Le salaire attendu</strong>. Eh oui, même en alternance, y a un petit boulot à faire à ce sujet. Pour en savoir plus, tu peux écouter notre podcast 
				<a href="https://soundcloud.com/user-986423909/podcast-1-negocier-son-salaire-cest-possible-meme-en-alternance">« Négocier son salaire, c’est possible même en alternance ».</a>
			</p>
			<p class="conseilsart">
				Si tu tombes sur une majorité d’annonces qui correspondent à 100% à tout ce que tu espères, alors c’est le jackpot pour toi ! Sinon, il faudra <strong>passer en revue tous ces 
				critères :</strong></p>
			<ul class="conseilsart">
				<li class="conseilsart">Lesquels sont indispensables et non-négociables ?</li>
				<li class="conseilsart">Lesquels sont prioritaires ?</li>
				<li class="conseilsart">Sur lesquels peux-tu faire des concessions ?</li>
			</ul>
			<p class="conseilsart">
				<strong>L’idée n’est pas de brader ton projet</strong> mais de le faire correspondre aux réalités du marché pour guider ta recherche vers le poste qui sera 1/ le plus en adéquation 
				avec tes aspirations 2/ le plus bénéfique pour ton projet à long terme. C’est toujours mieux d’avoir une vision bien claire de ton projet plutôt que d’avoir des vues sur un projet 
				un peu flou. Si tu veux une citation motivante et bien plus élégante : 
			</p>
			<p class="conseilsart">
				<em class="conseilsart">« The only thing worse than being blind is having sight but no vision » </em>C’est signé Helen Keller, sourde et aveugle depuis sa petite enfance & 
				première personne handicapée à avoir obtenu un diplôme universitaire aux États-Unis.
			</p>
			<h2 class="conseilsart"><strong>3. Réponds aux annonces avec méthode, audace et malice</strong></h2>
			<p class="conseilsart">Pour postuler à des offres de façon organisée et efficace, il faut : </p>
			<ul class="conseilsart">
				<li class="conseilsart">Bien connaître <strong>les sites d’emplois</strong> adaptés à ton métier, ton secteur et ta zone géographique. Certains sites sont hyper populaires dans 
					la tech et le digital, des nouveaux sont créés chaque mois… Renseigne-toi pour dénicher les perles.</li>
			    <li class="conseilsart">Créer un profil sur <strong>les sites de CVthèques</strong>.</li>
				<li class="conseilsart">Bien connaître <strong>les mots-clés utilisés par les recruteurs</strong> (d’où l’importance de lister toutes les déclinaisons possibles et imaginables 
					d’un intitulé de poste).</li>
			    <li class="conseilsart"><strong>Élargir sa recherche</strong> en fonction des critères moins prioritaires pour toi.</li>
				<li class="conseilsart"><strong>Personnaliser, personnaliser et encore personnaliser</strong>. Surtout si tu recherches un poste qui a plusieurs intitulés, ta candidature doit 
					toujours être en adéquation avec l’annonce concernée. <a href="#">Pour en savoir plus sur le CV et la lettre de motivation, on t’invite à lire notre article dédié.</a></li>
				<li class="conseilsart"><strong>Postuler à des offres de stages, des missions en freelance et des CDD/CDI pour des profils juniors</strong>, en ajoutant un couplet sur 
					l’alternance et ses points forts, pour tenter de faire passer le recruteur du côté obscur de la force.</li>
			    <li class="conseilsart"><strong>Bien organiser ses candidatures dans son ordi</strong> pour les retrouver facilement.</li>
				<li class="conseilsart"><strong>Faire un copier/coller de toutes les annonces auxquelles tu as répondu</strong>, car si un recruteur t’appelle et que cette dernière est 
					hors-ligne, bon courage pour te souvenir de tous les détails. </li>
				<li class="conseilsart"><strong>Créer un tracking des offres auxquelles tu as postulé</strong> (à quelle date, quel est le nom du recruteur + ses coordonnées, quelles sont les 
					éventuelles remarques/questions qui te sont venues à l’esprit en préparant ta candidature + toutes les infos qui peuvent t’être utiles).</li>
			</ul>
			<p class="conseilsart">
				Cependant, sois bien conscient d’une chose. C’est qu’on le fait toutes et tous, <strong>se jeter la tête la première dans le grand bain des offres d’alternance</strong>. Comme 
				les piscines en période de canicule, les annonces sont prises d’assaut par des centaines, voire des milliers de candidats tous aussi motivés que toi.
			</p>
			<p class="conseilsart">
				Est-ce que c’est une raison pour baisser les bras et ne pas affronter la concurrence ? NON. <br> Est-ce qu’il faut plutôt essayer de <strong>se démarquer pour retenir l’attention 
				des recruteurs</strong> ? OUI.
			</p>
			<p class="conseilsart">C’est pourquoi, avant de cliquer sur le bouton « Envoyer ma candidature », vérifie ces différents points :</p>
			<ul class="conseilsart">
				<li class="conseilsart">Je sais pourquoi je postule à cette offre et ce qui m’attire dans le poste & l’entreprise.</li>
				<li class="conseilsart">Mon CV met en avant des compétences en lien avec l’annonce.</li>
				<li class="conseilsart">Si elle est demandée, ma lettre de motivation est pertinente et intéressante à lire pour un recruteur. Si toi-même tu bailles en la relisant, imagine la 
					réaction d’un inconnu. </li> 	
			</ul>
			<h2 class="conseilsart"><strong>4. Aie le culot de tenter les candidatures spontanées</strong></h2>
			<p class="conseilsart">
				Tu as toujours ta feuille sur laquelle tu as décrit ton alternance de rêve ? Hop, attrape-la et démarre une autre liste : celle des boîtes que tu aimes pour X ou Y raisons. 
				Elles peuvent être multiples : 
			</p>
			<ul class="conseilsart">
				<li class="conseilsart"><strong>Tu adores les produits ou services et/ou les valeurs d’une entreprise</strong> et tu trouves que son approche sur le digital est intéressante. </li>
				<li class="conseilsart"><strong>Tu as lu un article ou écouté un podcast faisant intervenir un ou une expert(e)</strong> qui t’a vachement intéressé(e) ? Il se pourrait que de 
					bosser avec cette personne soit une piste à explorer.</li> 
				<li class="conseilsart"><strong>Tu connais quelqu’un qui bosse dans une boîte qui a l’air vraiment sympa</strong>, autant d’un point de vue projet que manégérial ?</li> 
				<li class="conseilsart">Si tu as envie de <strong>tenter l’aventure en start up</strong> ? Feuillette de tes doux clics les nombreux annuaires qui les recensent. </li>
				<li class="conseilsart">Si par exemple <strong>le bien-être au travail est capital pour toi</strong>, consulte la liste des entreprises récompensées d’un label comme Great Place 
					to Work. </li>
				<li class="conseilsart">Si tu vises plutôt u<strong>ne alternance en agence</strong> pour bosser sur des projets innovants, jette un oeil aux palmarès des Cannes Lions (ex-Lion 
					d’or). Beaucoup de catégories intègrent une forte dimension tech/digital.</li>
			</ul>
			<p class="conseilsart">
				Tu pourras ensuite <strong>mener ta petite enquête sur ces boîtes</strong>, pour savoir si elles pourraient correspondre à l’alternance que tu recherches, en fonction des 
				critères que tu as identifiés. Cela te donnera <strong>la motivation de tenter les candidatures spontanées</strong>, dans lesquelles tous les coups sont permis question 
				originalité & créativité !  En tapant à plusieurs portes avec un beau “toc toc”, il se peut qu’il y en ait une qui s’ouvre.
			</p>
			<p class="conseilsart">Les candidatures spontanées ont en effet plusieurs avantages :</p>
			<ul class="conseilsart">
				<li class="conseilsart">Pas d’annonce, donc pas de concurrence.</li>
				<li class="conseilsart">Si ça fonctionne, tu es sûr(e) de bosser pour un projet qui t’intéresse vraiment et d’avoir un poste sur mesure pour ton profil.</li>
				<li class="conseilsart">Si ça ne fonctionne pas, tu auras au moins marqué les esprits. Les candidatures spontanées, quand elles sont bien faites, laissent rarement leurs 
					destinataires indifférents.</li>
			</ul>
			<p class="conseilsart">
				Dernier conseil : Au fil de ces candidatures spontanées, si tu as de bons retours ou des échanges intéressants avec des recruteurs, ajoute-les absolument sur LinkedIn. Ils 
				pourront suivre ton parcours et qui sait, <strong>des opportunités pourront se présenter à l’avenir</strong>, une fois ton diplôme en poche !
			</p>

			<h2 class="conseilsart"><strong>5. Fais de LinkedIn ta meilleure arme</strong></h2>
			<p class="conseilsart">
				Si tu te marres parce que toi, tu as déjà un profil LinkedIn au top : ne skippe pas ce paragraphe trop vite. Good job pour ton profil, mais <strong>est-ce que tu es actif ?</strong>
			</p>
			<p class="conseilsart"><strong>Être actif sur LinkedIn</strong>, ça veut dire :</p> 
			<ul class="conseilsart">

				<li class="conseilsart">Créer des posts régulièrement (publications, articles, partages).</li>
				<li class="conseilsart">Répondre à des posts (donner son avis, participer à des débats etc.).</li>
				<li class="conseilsart">Ajouter des personnes en leur écrivant un message personnalisé.</li>
				<li class="conseilsart">Demander des conseils pour t’aider à définir ton projet professionnel (soit dans un post, soit en message privé).</li>
			</ul>
			<p class="conseilsart">
				Comme nous l’avons vu précédemment, il se peut que tu aies <strong>des doutes sur les tenants et les aboutissants de la carrière dans laquelle tu souhaites te lancer</strong>. 
				Des tas de pros sur LinkedIn sont passés par là avant toi et se feront un plaisir de t’aiguiller. Ne t’inquiète pas, en général, <strong>les gens adorent que l’on s’intéresse 
				à eux</strong> et parler d’eux, car c’est le sujet qu’ils maîtrisent le plus.
			</p>
			<p class="conseilsart">
				<strong>Dans le pire des cas</strong>, ils n’ont pas envie ou pas le temps de te répondre, ton message reste en « Vu ». Mais si tu fais preuve de politesse et de respect dans 
				ta demande, cela ne te portera pas préjudice. 
			</p>
			<p class="conseilsart">
				<strong>Dans le meilleur des cas</strong>, ils te répondent et ce sont des trésors d’information que tu pourras mobiliser pendant ta recherche d’alternance. 
			</p>
			<p class="conseilsart">
				<strong>Dans le meilleur du meilleur des cas</strong>, tu obtiens des contacts utiles pour booster ta recherche et ton réseautage. Alors là, c’est vraiment tout bénéf’.
			</p>
			
			<h2 class="conseilsart"><strong>6. Mobilise ton réseau de la vraie vie</strong></h2>
			<p class="conseilsart">
				Au delà des réseaux sociaux, il reste les vrais gens de la vraie vie. En cette période de confinement et de distanciation sociale, on a un peu oublié leur présence mais si, si 
				: ils sont toujours là ! 
			</p>
			<p class="conseilsart">
				Tes potes, ta famille, tes anciens collègues… <strong>Tu dois mettre au courant tes proches et ton entourage plus élargi de ta reconversion professionnelle</strong> et de ce 
				que tu recherches. Sans les saouler non plus, on est d’accord. Mais souvent, c’est ce qui fonctionne le mieux et le plus rapidement.
			</p>
			<p class="conseilsart">
				<strong>Dans toute relation humaine, il y a plus d’émotionnel et plus de force de frappe</strong>. Entre un recruteur qui épluche ton CV presque machinalement parmi 50 autres 
				et un employé en poste depuis quelques années, à qui l’on fait totalement confiance, qui parle de toi à son équipe pour savoir si quelqu’un connaît quelqu’un qui recrute… 
				Quelle situation aura le plus d’impact et te sera la plus favorable ? 
			</p>
			<p class="conseilsart">
				Même si ça ne donne rien, c’est toujours un bon exercice pour t’entraîner à l’entretien d’embauche. En parlant de façon informelle à tes proches, tu auras l’occasion de 
				présenter sans pression tes motivations pour te lancer dans une alternance, de justifier les raisons de ta reconversion professionnelle et de parler de ton affection pour 
				la tech ou le digital.
			</p>
			<p class="conseilsart">
				En complément, on t’a préparé un podcast pour aller encore plus loin : <a href="https://soundcloud.com/user-986423909/podcast-2-le-marche-cache-des-offres-dalternance">« 
				Le marché caché des offres d’alternance ».</a>
			</p>
			<p class="conseilsart">
				Merci d’avoir lu cet article jusqu’au bout ! <br> Tu l’auras compris, il n’existe <strong>pas de recette magique</strong> pour qu’un contrat d’alternance te tombe dans les 
				mains du jour au lendemain. <strong>Il va falloir bosser, s’organiser et surtout, ne pas hésiter à sortir des sentiers battus pour te démarquer.</strong>
			</p>
			<p class="conseilsart">
				On espère que ça t’a donné plein d’idées et que ça t’a motivé pour booster ta recherche d’alternance. Pour aller plus loin, consulte nos autres conseils ou nos podcasts.
			</p>
			<p class="conseilsart">
				On te souhaite tout le meilleur pour ta recherche et si tu as quelques secondes, <strong>partage cet article</strong> pour qu’il puisse aider d’autres aspirants alternants.
