			<title class="conseilsart">Perfectionner son CV et sa lettre de motivation pour postuler à une alternance</title>
			<img src="medias/article2.jpg" class="conseilsart">
			<p class="conseilsart">
				Quand on commence à <strong>postuler massivement à des offres d’alternance</strong> ou à <strong>envoyer des candidatures spontanées</strong>, on met du cœur à l’ouvrage 
				pour <strong>créer un CV et une lettre de motivation les plus sérieux possibles</strong>. Assez sérieux pour dire « embauchez-moi, je suis motivé(e) et sympa ! », en 
				espérant que quelqu’un morde à l’hameçon. On est un peu Sting époque The Police : <em class="conseilsart">« I'll send an SOS to the world, I hope that someone gets my 
				message in a bottle, yeaaah ».</em> 
			</p>
			<p class="conseilsart">	Le CV, c’est ta bouteille : si elle a l’air en piteux état, qu’on n’a pas envie de la toucher, peu importe ce qu’il y a à l’intérieur, elle fera l’effet 
				d’un coup d’épée dans l’eau. La lettre de motivation, c’est le message renfermé précieusement à l’intérieur, qu’on lira seulement si la bouteille a donné envie d’être ouverte. 
				C’est pour ça qu’il faut <strong>mettre le paquet sur les 2 ! Une candidature parfaite, c’est un CV et une lettre de motivation qui ont poussé un recruteur à te contacter 
				pour un poste qui correspond à tes aspirations</strong>. Ça veut qu’il n’y a pas de candidature parfaite en soit, mais une candidature qui marchera parfaitement avec un 
				job précis dans une boîte donnée. 
			</p>
			<p class="conseilsart">
				Attention, cet article ne va pas t’apprendre à <strong>créer un CV à A à Z ou à écrire une lettre de motivation</strong>. Tape « faire un CV » sur Google, c’est 150 millions 
				de résultats. La lettre de motivation, cette éternelle mal aimée, n’est qu’aux alentours de 45 millions de résultats. On ne se mouille donc pas trop en te disant que <strong>
				tu trouveras tout ce qu’il te faut sur le web pour avoir des tas de conseils là-dessus</strong>. Dans toutes les langues, des plus ou moins pertinents, des plus ou moins récents.
			</p>
			<p class="conseilsart">
				Ici, on s’adresse à <strong>toi qui as déjà ton CV prêt à être dégainé</strong> et toi qui as <strong>déjà une lettre de motivation</strong> sous le coude. Tu maîtrises les 
				basiques de ces 2 exercices et tu cherches plutôt <strong>des idées pour pimper tes candidatures et les rendre plus performantes</strong>. Surtout si tu te lances en alternance 
				dans la tech ou le digital et pour un projet de reconversion professionnelle, ces conseils te sont tout particulièrement destinés.
			</p>
			<h2 class="conseilsart"><strong>1. Pense « compétences »</strong></h2>
			<p class="conseilsart">L’alternance a cette spécificité de concerner deux types de profil : </p>
			<ul class="conseilsart">
				<li class="conseilsart">Le débutant post-bac qui n’a pas ou peu d’expérience professionnelle.</li>
				<li class="conseilsart">La personne qui a déjà travaillé pendant plus ou moins longtemps, désormais en reconversion. Elle a des expériences à mettre sur la table, mais 
					celles-ci ne correspondent pas vraiment au poste recherché. </li>
			</ul>
			<p class="conseilsart">
				Dans ces cas-là, comment valoriser des compétences qui « n’existent pas » ou qui « ne sont pas en phase » avec les demandes des recruteurs ? La première chose à faire, 
				c’est de <strong>mettre cette idée qu’on n’a pas les compétences demandées et qu’on n’a rien à dire à la poubelle</strong>. Si on a choisi cette voie, c’est qu’on s’en 
				sent capable, qu’on est motivé à apprendre un nouveau métier et qu’on pense pouvoir réussir. OK ? 
			</p>
			<p class="conseilsart">
				Traditionnellement en France, c’est vrai que l’on valorise plutôt l’expérience professionnelle, par ordre antéchronologique, avec de jolis bullet points pour détailler 
				chaque mission. Heureusement,<strong>les mentalités évoluent et on peut maintenant s’inspirer de nos amis anglo-saxons qui sont très focus sur les compétences.</strong>
			</p>
			<p class="conseilsart">
				Pour y voir plus clair sur les compétences liées à ton futur métier, <strong>synthétise plusieurs annonces qui correspondent au poste que tu vises</strong>>: quelles 
				sont les compétences techniques les plus souvent demandées (hard skills) ? Quels sont les savoir-être évoqués (soft skills) ? 
			</p>
			<p class="conseilsart">Par exemple, on demande souvent aux développeurs web :</p>
			<ul class="conseilsart">
				<li class="conseilsart">De maîtriser la techno utilisée sur le projet.</li>
				<li class="conseilsart">D’être hyper rigoureux et méthodique.</li>
				<li class="conseilsart">De savoir s’adapter à tout type de contraintes.</li>
				<li class="conseilsart">D’être en veille sur les évolutions des techno et curieux de les apprendre.</li>
				<li class="conseilsart">De travailler en collaboration avec des équipes techniques et non techniques.</li>
			</ul>
			<p class="conseilsart">
				<strong>Tu ne pourras pas gruger sur les compétences techniques</strong> et prétendre que tu maîtrises tel ou tel langage si tu débutes. Reste honnête à propos de ça. 
				En revanche, <strong>ce n’est pas parce que tu n’as jamais codé dans un cadre professionnel que tu n’es pas rigoureux et méthodique</strong>. Un expert comptable n’aura 
				sans doute aucun mal à faire le pont entre la méticulosité dont il devait faire preuve dans son ancien métier et les exigences du code.
			</p>
			<p class="conseilsart">
				Idem pour la veille, rares sont les métiers où elle n’existe pas. N’hésite pas à citer les médias (sérieux) que tu consultes régulièrement ou les podcasts que tu écoutes 
				par exemple. Tout ce qui démontre <strong>un goût prononcé pour ton nouveau métier</strong> sera un gros plus dans ta candidature, même si ça semble anodin au premier 
				abord ! L’essentiel, c’est de bien savoir faire le lien entre toi, ta vie, ton œuvre & le job que tu vises. 
			</p>
			<p class="conseilsart">
				Pour valoriser tes précédentes expériences, même si a priori, elles n’ont rien à voir avec la tech ou le digital, n’hésite pas à les rassembler et les expliciter dans 
				<strong>une rubrique dédiée de ton CV et visible au premier coup d’œil</strong>. Différencie bien les soft skills et les hard skills pour plus de lisibilité. Essaie 
				d’<strong>être court et efficace</strong>, tu pourras développer davantage dans ta lettre de motivation. Si tu détailles ces compétences en bullet points dans tes 
				précédentes expériences pro, et non dans une rubrique dédiée, elles ne seront pas lues car l’intitulé du poste n’aura pas laissé au recruteur la chance de s’y attarder.
			</p>
			<h2 class="conseilsart"><strong>2. Montre ta personnalité</strong></h2>
			<p class="conseilsart">
				Des compétences bien présentées, bien valorisées et bien argumentées, c’est un premier gros plus pour ta candidature qui te permettra d’être repéré par un recruteur. 
				Ce qui risque de se produire néanmoins, c’est que ce même recruteur reçoive plus de 100 candidatures et que <strong>75% d’entre elles présentent toutes des compétences 
				en adéquation avec le poste.</strong>
			</p>
			<p class="conseilsart">
				Ce qui va guider le recruteur dans sa pré-sélection, c’est <strong>la personnalité que laisseront transparaître chacune de ces candidatures</strong>. Il se demandera 
				comment la personnalité qu’il décèle pourra s’intégrer à l’entreprise et faire le job demandé, voire apporter quelque chose de nouveau et d’intéressant.
			</p>
			<p class="conseilsart">
				Ce que tu vas montrer de ta personnalité, <strong>c’est à double tranchant</strong>. Les conseils qui suivent ne conviendront pas à tous les cas de figure, manie-les 
				avec prudence. Dans certaines entreprises à l’ancienne, mieux vaut rester formel et lisse, tout concentrer dans les compétences. Par contre, <strong>si tu sens que 
				l’entreprise recherche des personnes plutôt que des diplômes, lance-toi.</strong>
			</p>
			<p class="conseilsart">
				Mais n’oublie pas que tout est une question de mesure et qu’une candidature n’est pas une biographie détaillée. C’est <strong>un outil de communication qui résume ton 
				parcours professionnel et laisse deviner le genre de collègue que tu es !</strong> Si ce qui transparaît de ta personnalité te disqualifie, c’est peut-être que l’ambiance 
				de travail ne t’aurait pas convenu ou que tu n’aurais pas été dans le même mood que l’équipe.
			</p>
			<h3 class="conseilsart">En cas de reconversion professionnelle</h3>
			<p class="conseilsart">
				<strong>Aborde franchement la question de ta reconversion professionnelle</strong>, ne la garde pas au chaud pour l’entretien. Souvent, les raisons d’une reconversion 
				ne sont pas mentionnées clairement dans les candidatures. Il faut pourtant les expliciter brièvement sur le CV et en quelques phrases dans la lettre de motivation, pour 
				montrer aux recruteurs que c’était <strong>une décision mûrement réfléchie</strong> et les rassurer sur le sérieux de ton investissement.
			</p>
			<h3 class="conseilsart">Les valeurs</h3>
			<p class="conseilsart">
				Appuie toi sur les valeurs. Tu as de la chance, c’est à la mode pour les marques d’en faire des caisses sur leurs valeurs. Idem pour les marques employeurs, elles n’échappent 
				pas à la tendance. Accroche-toi à la branche, par le prisme de ces fameuses valeurs, <strong>explique ce que tu as compris de l’entreprise et ce qui fait que tu as envie 
				de la rejoindre.</strong>
			</p>
			<p class="conseilsart">
				Essaie de ne pas les employer comme des mots fourre-tout, que tout le monde utilise à toutes les sauces, à tort et à travers. Tu dois mettre du sens derrière. Une façon 
				plutôt simple de faire ça, c’est de <strong>décrire avec tes mots à toi ce que ces valeurs représentent à tes yeux.</strong>
			</p>
			<p class="conseilsart">
				Exemple : <strong>le travail d’équipe est une valeur fondamentale pour l’entreprise à laquelle tu postules</strong>. Son site est rempli de photos d’équipe où tout le monde 
				sourit, la bonne ambiance est prônée, les projets se font en équipe, des outils de travail collaboratifs sont cités dans l’annonce… Bref, tu as bien compris à quel point 
				c’est important pour eux.
			</p>
			<p class="conseilsart">
				Dans ce cas-là, ta lettre ou ton mail de motivation mérite un paragraphe sur <strong>ce qu’est le travail d’équipe pour toi, ses avantages et ses points plus délicats	
				</strong> (reste modéré cependant). <strong>Cite un exemple d’une expérience</strong> où tu as aimé travailler en équipe, ce qu’elle t’a apporté. Le but, c’est <strong>
				d’aider le recruteur à te visualiser comme un membre de l’équipe</strong> qu’il essaie d’agrandir. Plus tu te fonds dans le décor, mieux c’est !
			</p>
			<h3 class="conseilsart">La rédaction de la lettre de motivation</h3>
			<p class="conseilsart">
				<strong>Bannis les formules toutes faites</strong> que l’on retrouve dans 85% des lettres de motivation. Le PIRE du PIRE, ce sont les modèles de lettre de motivation… 
				pour les candidatures spontanées.
			</p>
			<p class="conseilsart">
				<strong>Utilise tes mots à toi</strong> (encore une fois !), aies les idées bien claires avant de rédiger quoi que ce soit, fais des phrases simples. Même si tu n’es pas 
				Victor Hugo, tu verras qu’au fur et à mesure, tu seras de plus en plus à l’aise et de plus en plus percutant dans tes formulations. 
			</p>
			<p class="conseilsart">
				<strong>Si tu es junior, surtout pas de tartine</strong>. Mets en lumière tes compétences et ta volonté de t’impliquer dans un projet dans lequel tu crois. Évite de 
				développer longuement des expériences qui n’ont vraiment aucun rapport avec le job visé, si tu ne peux pas en dégager quelque chose de réellement positif.
			</p> 
			<h3 class="conseilsart">La mise en page du CV</h3>
			<p class="conseilsart">
				Bon les copains, on va arrêter de se voiler la face : <strong>un CV joli à regarder marque plus de points</strong>, surtout si le contenu est bon. C’est vrai, on n’est pas 
				tous graphistes ceinture noire de Photoshop & Indesign, mais il faut arrêter avec les CV 100% texte, écrits à l’arrache sur Word ou PowerPoint avec de la Times New Roman.
			</p>
			<p class="conseilsart">
				<strong>Des templates accessibles et gratuits existent</strong>. Les meilleurs sont sans doute sur l’outil en ligne Canva. Même sans compétence en design, on peut facilement 
				remanier un modèle de CV à sa sauce, en choisissant les couleurs, la police, l’ordre des rubriques etc. Comme pour tout, il faut <strong>rester simple et efficace</strong>. 
			</p>
			<h2 class="conseilsart"><strong>3. Soigne les petits détails</strong></h2>
			<p class="conseilsart">
				Pas pour toi, mais pour les recruteurs. Ta candidature c’est comme un bon café. Les petits détails, c’est les petits biscuits à côté de la tasse. On ne les demande pas, 
				mais quand ils sont là, ça fait toujours plaisir et c’est plus savoureux.
			</p>
			<h3 class="conseilsart">L’orthographe</h3>
			<p class="conseilsart">
				On t’avait promis d’éviter les conseils bateau déjà rabachés 150 millions de fois, mais celui-là, on ne le répètera jamais assez. Même si c’est vrai que de plus en plus 
				d’annonces sont publiées avec des fautes, <strong>une candidature sans faute d’orthographe, de grammaire et de syntaxe n’a jamais pénalisé personne</strong>. Le contraire, 
				si. On ne peut pas connaître à l’avance le niveau de sensibilité du recruteur, alors ça vaut peut-être le coup de te forcer à t’en occuper. Bon courage, c’est galère 
				pour tout le monde ! 
			</p>
			<h3 class="conseilsart">Le nom des fichiers envoyés</h3>
			<p class="conseilsart">
				Les noms de type MonCVversion36.ppt ou brouillonLM.doc, c’est non. Choisis des noms de fichiers clairs et professionnels. Aussi, rien de plus désagréable pour un recruteur 
				que de recevoir un fichier CV_NomduCandidat_EntrepriseX alors que c’est l’Entreprise Y ! Personne n’aime être rappelé au bon souvenir de son concurrent. On ne parlera pas 
				non plus des oublis de pièce jointe si tu contactes directement le recrutement par email, no comment.  
			</p>
			<h3 class="conseilsart">Le format des fichiers envoyés</h3>
			<p class="conseilsart">
				On va trancher le débat et le dire haut et fort une bonne fois pour toute : <strong>PDF only</strong>.  Ce n’est pas trop lourd en terme de poids et ça ne fera pas sauter 
				ta mise en page. À la différence des PNG/JPEG, le PDF permettra aux recruteurs de pouvoir faire quelques copier-coller d’infos importantes (comme ton email ou ton numéro 
				de téléphone par exemple). Beaucoup plus pratique pour lui.
			</p>
			<h3 class="conseilsart">Les couleurs</h3>
			<p class="conseilsart">
				Il y a toute une science autour de ça, on te laisse te renseigner de ton côté. Par contre, quand tu postules pour un job coup de coeur, est-ce que ça ne vaudrait pas le 
				coup de mettre ta candidature aux couleurs de l’entreprise, en guise de clin d’œil ? En bidouillant les codes HEXA, ça peut se faire en quelques minutes.
			</p>
			<h3 class="conseilsart">L’harmonie entre ton CV et ta lettre de motivation</h3>
			<p class="conseilsart">
				Pense à accorder la mise en page de ta lettre de motivation à la couleur dominante de ton CV. Un liseré pour encadrer la page ou quelques touches de couleur par ci, par 
				là te permettront de créer un univers visuel qui t’est propre et qui est propre. Idem pour la police, utilise la même pour les 2 documents et pas de police fantaisie bien entendu. 
			</p>
			<p class="conseilsart">
				Voilà pour ces conseils qui peuvent t’aider à perfectionner ton CV et/ou ta lettre de motivation. Merci d’avoir lu cet article jusqu’au bout. 
			</p>
			<p class="conseilsart">
				Postuler à des offres d’alternance ou même en candidature spontanée est toujours un exercice délicat, car on ne sait jamais par qui elles seront lues à l’autre bout du wifi. 
				Mais si le recruteur voit  du potentiel dans ta candidature, c’est que tu auras fait un bon boulot !
			</p>
			<p class="conseilsart">
				On espère que ça t’a donné plein d’idées et que ça t’a motivé pour booster ta recherche d’alternance. Pour aller plus loin, consulte nos autres conseils ou nos podcasts.
			</p>
			<p class="conseilsart">
				On te souhaite tout le meilleur pour ta recherche et si tu as quelques secondes, <strong>partage cet article</strong> pour qu’il puisse aider d’autres aspirants alternants.
			</p>
